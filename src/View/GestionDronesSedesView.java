    private void btnRegistrarSedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarSedeActionPerformed
     if (txtCodigoSede.getText().trim().isEmpty() || 
        txtNombreSede.getText().trim().isEmpty() || 
        txtCiudadSede.getText().trim().isEmpty()) { 
        
        JOptionPane.showMessageDialog(this, "Completa los campos de la sede.");
        return;
    }
    
    // Asumimos un valor por defecto o creamos un campo exclusivo para capacidad
    int capacidad = 0; 
    try {
        capacidad = Integer.parseInt(txtCapacidadSede.getText());
    } catch (NumberFormatException e) {
        // Si no es un número, dejamos 0 o lanzamos aviso
    }

    Data.sedes.add(new Sede(txtCodigoSede.getText(), txtNombreSede.getText(), txtCiudadSede.getText(), capacidad));
    actualizarTablas();
    actualizarComboSedes();
    JOptionPane.showMessageDialog(this, "Sede registrada.");

    
    }//GEN-LAST:event_btnRegistrarSedeActionPerformed

    private void btnRegistrarDronActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarDronActionPerformed

    if (cmbSedes.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Selecciona una sede válida.");
            return;
        }

        String nombreSede = cmbSedes.getSelectedItem().toString();
        Sede sedeAsignada = null;
        for (Sede s : Data.sedes) {
            if (s.getNombre().equalsIgnoreCase(nombreSede)) {
                sedeAsignada = s;
                break;
            }
        }

        try {
            double autonomia = Double.parseDouble(txtCapacidadSede.getText().trim());
            Data.drones.add(new DronEstandar(txtCodigoDron.getText(), txtModeloDron.getText(), autonomia, "Disponible", "Estándar", sedeAsignada));
            actualizarTablas();
            JOptionPane.showMessageDialog(this, "Dron registrado correctamente.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: La autonomía debe ser un número.");
        }
    
    }//GEN-LAST:event_btnRegistrarDronActionPerformed

