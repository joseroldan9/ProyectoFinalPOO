    // Este método lo puedes llamar cuando quieras ver los datos (ej. al presionar un botón)
   public void actualizarTablas() {
    DefaultTableModel modelo = (DefaultTableModel) tablaInventario.getModel();
    modelo.setRowCount(0); // Esto borra todas las filas actuales de la tabla en pantalla

    // Volvemos a insertar solo lo que está en las listas actuales de Data
    for (Sede s : Data.sedes) {
        modelo.addRow(new Object[]{"Sede", s.getCodigo(), s.getNombre(), s.getCiudad(), "Disponible"});
    }

    for (Dron d : Data.drones) {
        // Lógica para obtener el nombre de la sede del dron
        String nombreSede = (d instanceof DronEstandar && ((DronEstandar) d).getSede() != null) 
                            ? ((DronEstandar) d).getSede().getNombre() : "N/A";
        
        modelo.addRow(new Object[]{"Dron", d.getCodigo(), d.getModelo(), nombreSede, (int)d.getNivelBateria() + " min"});
    }
}

    private void inicializarSedesMundial() {
        String[] sedesMundial = {
            "MetLife Stadium", "SoFi Stadium", "AT&T Stadium", "Arrowhead Stadium", 
            "Mercedes-Benz Stadium", "NRG Stadium", "Lincoln Financial Field", 
            "Gillette Stadium", "Levi's Stadium", "Lumen Field", "Hard Rock Stadium", 
            "Estadio Azteca", "Estadio BBVA", "Estadio Akron"
        };

        for (String nombre : sedesMundial) {
            boolean existe = false;
            for (Sede s : Data.sedes) {
                if (s.getNombre().equalsIgnoreCase(nombre)) {
                    existe = true;
                    break;
                }
            }
            if (!existe) {
                Data.sedes.add(new Sede("ID-" + nombre.substring(0, 3).toUpperCase(), nombre, "Sede Mundial", 0));
            }
        }
    }

    private void actualizarComboSedes() {
        cmbSedes.removeAllItems();
        cmbSedes.addItem("Seleccione una sede...");
        for (Sede s : Data.sedes) {
            cmbSedes.addItem(s.getNombre());
        }
    }
   
  
